package wt.seckill.dao;

import java.util.Date;
import java.util.List;

import javax.annotation.Resource;

import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;

import wt.seckill.entity.Seckill;

/**
 * 
 * @author hohoTT
 * 配置 spring 与 junit 的整合，junit 启动时加载springIOC容器 -- @RunWith
 * spring-test,junit
 */
@RunWith(SpringJUnit4ClassRunner.class)
// 告诉 junit spring 的配置文件
@ContextConfiguration({"classpath:spring/spring-dao.xml"})
public class SeckillDaoTest {
	
	// 注入 Dao 实现类依赖
	@Resource
	private SeckillDao seckillDao;
	
	@Test
	public void testQueryById() throws Exception {
		long id = 1000;
		Seckill seckill = seckillDao.queryById(id);
		System.out.println(seckill.getName());
		System.out.println(seckill);
	}
	
	@Test
	public void testQueryAll() throws Exception {
		// java 没有保存形参的记录
		// 举例 ： queryAll(int offset, int limit) - > queryAll(arg0, arg1)
		
		
		List<Seckill> seckills = seckillDao.queryAll(0, 100);
		
		for (Seckill seckill : seckills) {
			System.out.println(seckill);
		}
		
	}
	
	@Test
	public void testReduceNumber() throws Exception {
		Date killTime = new Date();
		
		int updateCount = seckillDao.reduceNumber(1000, killTime);
		
		System.out.println("updateCount ---- " + updateCount);
	}

}
